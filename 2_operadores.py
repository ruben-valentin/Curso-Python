### Operadores Comparativos ###

print(3 > 4)  # Compara si 3 es mayor que 4. Imprime: False
print(3 < 4)  # Compara si 3 es menor que 4. Imprime: True
print(3 >= 4)  # Compara si 3 es mayor o igual que 4. Imprime: False
print(4 <= 4)  # Compara si 4 es menor o igual que 4. Imprime: True
print(3 == 4)  # Compara si 3 es igual a 4. Imprime: False
print(3 != 4)  # Compara si 3 es diferente de 4. Imprime: True

print(numero1 != numero2)  # Compara si 3 es diferente de 4. Imprime: True
